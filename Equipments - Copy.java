    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        ArrayList<PlayersModel> list = PlayersDao.getAllRecords();
        Iterator<PlayersModel> itr = list.iterator();
        while (itr.hasNext()) {
            PlayersModel playersObj = itr.next();
            txtPlayerID.addItem(playersObj.getPlayerID());
        }
        
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        ArrayList<EquipmentsModel> list2 = EquipmentsDao.getAllRecords();
        Iterator<EquipmentsModel> itr2 = list2.iterator();
        while (itr2.hasNext()) {
            EquipmentsModel equipmentsObj = itr2.next();
            dtm.addRow(new Object[]{equipmentsObj.getEquipmentID(), equipmentsObj.getEquipmentName(), equipmentsObj.getQuantity(), equipmentsObj.getPlayerID()});
        }
    }//GEN-LAST:event_formComponentShown

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        String equipmentID = model.getValueAt(index, 0).toString();
        String equipmentName = model.getValueAt(index, 1).toString();
        int a = JOptionPane.showConfirmDialog(null, "Do you want to Delete " + equipmentName, "Select", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            EquipmentsDao.delete(equipmentID);
            clear();
        }
    }//GEN-LAST:event_jTable1MouseClicked

