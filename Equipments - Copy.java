    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String equipmentID = txtEquipmentID.getText();
        ArrayList <EquipmentsModel> list = EquipmentsDao.search(equipmentID);
        Iterator<EquipmentsModel> itr = list.iterator();
        while (itr.hasNext()) {
            EquipmentsModel equipmentsObj = itr.next();
            txtEquipmentID.setText(equipmentsObj.getEquipmentID());
            txtEquipmentName.setText(equipmentsObj.getEquipmentName());
            txtQuantity.setText(Integer.toString(equipmentsObj.getQuantity()));
            txtPlayerID.setSelectedItem(equipmentsObj.getPlayerID());
        }
        btnInsert.setEnabled(true);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_btnSearchActionPerformed

