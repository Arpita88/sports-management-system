    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        String coachID = model.getValueAt(index, 0).toString();
        String coachName = model.getValueAt(index, 1).toString();
        int a = JOptionPane.showConfirmDialog(null, "Do you want to Delete " + coachName, "Select", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            CoachDao.delete(coachID);
            clear();
//            setVisible(false);
//            new Coach().setVisible(true);
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        ArrayList<CoachModel> list = CoachDao.getAllRecords();
        Iterator<CoachModel> itr = list.iterator();
        while (itr.hasNext()) {
            CoachModel coachObj = itr.next();
            dtm.addRow(new Object[]{coachObj.getCoachID(), coachObj.getCoachName(), coachObj.getGender(), coachObj.getExperience(), coachObj.getSportsName()});
        }
    }//GEN-LAST:event_formComponentShown

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String coachID = txtCoachID.getText();
        ArrayList <CoachModel> list = CoachDao.search(coachID);
        Iterator<CoachModel> itr = list.iterator();
        while (itr.hasNext()) {
            CoachModel coachObj = itr.next();
            txtCoachID.setText(coachObj.getCoachID());
            txtCoachName.setText(coachObj.getCoachName());
            txtGender.setSelectedItem(coachObj.getCoachName());
            txtExperience.setText(Integer.toString(coachObj.getExperience()));
            txtSportsName.setText(coachObj.getSportsName());
        }
        btnInsert.setEnabled(true);
        btnUpdate.setEnabled(true);
        
    }//GEN-LAST:event_btnSearchActionPerformed

