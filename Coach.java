    public void validateField() {
        String coachID = txtCoachID.getText();
        String coachName = txtCoachName.getText();

        String experience = txtExperience.getText();
        String sportsName = txtSportsName.getText();

        if (!coachID.equals("") && !coachName.equals("") && !experience.equals("") && !sportsName.equals("")) {
            btnInsert.setEnabled(true);
            btnUpdate.setEnabled(true);
        } else {
            btnInsert.setEnabled(false);
            btnUpdate.setEnabled(false);
        }
    }
    
    

